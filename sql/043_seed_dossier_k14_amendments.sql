-- =============================================
-- Миграция 043: Дополнительные соглашения к Договору генподряда №К14 от 25.04.2025 г.
-- Проект: ЖК PRIMAVERA квартал Bellini. К14
-- Застройщик: ООО «СЗ «Стадион «Спартак» (Савин Н.О.)
-- Генподрядчик: ООО «СУ-10» (Товбин М.С.)
--
-- Входят ДС: №1 (2025, без даты), №4 (01.10.2025), №7 (16.02.2026), №8 (24.02.2026)
--
-- Важно: mergeJson (useDossier.ts) перезаписывает поля базового договора ЛЮБЫМИ
-- непустыми значениями из ДС. Поэтому в jsonb-блоках amendment'а указываем ТОЛЬКО
-- реально изменённые ключи, чтобы не «откатить» правки более ранних ДС.
-- =============================================

-- Удаляем ранее загруженные ДС по К14 — миграция идемпотентна
delete from contract_dossiers
where document_type = 'amendment'
  and project_id in (
    select project_id from contract_dossiers
    where document_type = 'contract'
      and document_number = '№К14'
  )
  and document_number in ('ДС №1', 'ДС №4', 'ДС №7', 'ДС №8');

-- ============================================================
-- ДС №1 (2025, дата подписания не указана)
-- Разовое разрешение на выплату Аванса до 170 000 000 ₽ БЕЗ Банковской Гарантии
-- (отступление от п.22.1.1) под поручительство Владимирова И.В. (ИНН 774314103512).
-- Структурные поля досье не меняются — все jsonb-блоки пустые.
-- ============================================================
insert into contract_dossiers (
  project_id, document_type, document_number, document_date,
  header_data, bdds_data, bdr_data, penalties_data,
  amendment_summary, is_active
)
select
  base.project_id,
  'amendment',
  'ДС №1',
  null,
  '{}'::jsonb,
  '{}'::jsonb,
  '{}'::jsonb,
  '{}'::jsonb,
  'Разрешена выплата Аванса до 170 000 000 ₽ (в т.ч. НДС 20% — 28 333 333 ₽) БЕЗ Банковской Гарантии (п.22.1.1) при условии поручительства физического лица — Владимирова Ильи Владиславовича (ИНН 774314103512). Разовое отступление от условия БГ на аванс.',
  true
from contract_dossiers base
where base.document_type = 'contract'
  and base.document_number = '№К14';

-- ============================================================
-- ДС №4 от 01.10.2025 — изменение цены договора (п.13.1.1.1)
-- Новая цена: 15 783 711 735,72 ₽ вкл. НДС 20% (было 15 800 000 000 ₽).
-- В header меняется ТОЛЬКО contract_amount.
-- ============================================================
insert into contract_dossiers (
  project_id, document_type, document_number, document_date,
  header_data, bdds_data, bdr_data, penalties_data,
  amendment_summary, is_active
)
select
  base.project_id,
  'amendment',
  'ДС №4',
  '2025-10-01'::date,
  jsonb_build_object('contract_amount', 15783711735.72::numeric),
  '{}'::jsonb,
  '{}'::jsonb,
  '{}'::jsonb,
  'Новая цена Договора — 15 783 711 735,72 ₽ вкл. НДС 20% (включая ГУ 2,5%). Новая редакция Приложения №1 «Протокол договорной цены» и Приложения №4 «График финансирования Работ».',
  true
from contract_dossiers base
where base.document_type = 'contract'
  and base.document_number = '№К14';

-- ============================================================
-- ДС №7 от 16.02.2026 — изменение сроков (п.8.1.3)
-- Начало СМР          — 01.05.2025 (совпадает с базой — не включаем)
-- Окончание СМР       — 30.12.2027
-- Получение ЗОС       — 01.02.2028
-- Ввод в эксплуатацию — 01.04.2028
-- Окончание передачи  — 01.10.2028  ← берём как header.end_date
--
-- В header меняем end_date и duration_months.
-- СМР/ЗОС/ввод/передача целиком фиксируются в amendment_summary
-- (в текущей схеме IDossierHeaderData отдельных полей для них нет).
-- ============================================================
insert into contract_dossiers (
  project_id, document_type, document_number, document_date,
  header_data, bdds_data, bdr_data, penalties_data,
  amendment_summary, is_active
)
select
  base.project_id,
  'amendment',
  'ДС №7',
  '2026-02-16'::date,
  jsonb_build_object(
    'end_date', '2028-10-01',
    'duration_months', 41
  ),
  '{}'::jsonb,
  '{}'::jsonb,
  '{}'::jsonb,
  'Новые сроки (п.8.1.3): начало СМР — 01.05.2025, окончание СМР — 30.12.2027, ЗОС — 01.02.2028, ввод в эксплуатацию — 01.04.2028, окончание Мероприятий по передаче — 01.10.2028. Новая редакция Приложения №3 «График выполнения Работ».',
  true
from contract_dossiers base
where base.document_type = 'contract'
  and base.document_number = '№К14';

-- ============================================================
-- ДС №8 от 24.02.2026 — НДС «по применимой ставке» (п.13.1.1)
-- Сумма Договора не меняется относительно ДС №4 (15 783 711 735,72 ₽).
-- «НДС 20%» заменён на «НДС по применимой ставке, определяемой
-- законодательством в соответствующем периоде» — применяется с 01.01.2026
-- (п.2 ст.425 ГК РФ). Новая редакция Приложения №1.
--
-- В текущей схеме нет поля под «переменную» НДС-ставку: nds_rate — число.
-- Структурно ДС №8 ничего не меняет (contract_amount уже изменён ДС №4),
-- вся суть фиксируется в amendment_summary.
-- ============================================================
insert into contract_dossiers (
  project_id, document_type, document_number, document_date,
  header_data, bdds_data, bdr_data, penalties_data,
  amendment_summary, is_active
)
select
  base.project_id,
  'amendment',
  'ДС №8',
  '2026-02-24'::date,
  '{}'::jsonb,
  '{}'::jsonb,
  '{}'::jsonb,
  '{}'::jsonb,
  'НДС изменён с «20%» на «по применимой ставке, определяемой законодательством в соответствующем периоде» (п.13.1.1 и все Приложения к Договору). Применяется к отношениям Сторон с 01.01.2026 (п.2 ст.425 ГК РФ). Сумма Договора не меняется — 15 783 711 735,72 ₽. Новая редакция Приложения №1 «Протокол договорной цены».',
  true
from contract_dossiers base
where base.document_type = 'contract'
  and base.document_number = '№К14';
