-- 055: Расширение уникального индекса дедупликации etl_1c_entries.
--
-- Контекст: при сверке р/с 40702810225644213694 с карточкой сч.51 обнаружено,
-- что 16 строк не попадают в БД. Причина — в карточке сч.51 один и тот же
-- СП-документ может породить несколько проводок с ОДИНАКОВОЙ суммой
-- (платёж разбит по нескольким накладным/субсчетам).
-- Конкретные примеры из карточки за период 01.01.2022 — 22.04.2026:
--   • СП00-011397 от 30.06.2025 — 6 строк по 354 188,38 ₽ (компенсация эл-эн.);
--   • СП00-005212 от 31.03.2026 — 12 строк по 258 000,00 ₽ (СЗ ИОНЖ, ЖБИ).
-- Текущий ключ дедупа (045) включает document, но не различает строки
-- одного документа с одинаковой суммой → теряется баланс на ~4,6 млн ₽.
--
-- Решение: добавить в ключ дедупа analytics_dt и analytics_kt — они уникальны
-- на каждой проводке (разные накладные/субсчета 60.01/60.02/76.05).

DROP INDEX IF EXISTS etl_1c_entries_dedup_idx;

CREATE UNIQUE INDEX IF NOT EXISTS etl_1c_entries_dedup_idx
  ON etl_1c_entries (
    doc_date,
    amount,
    COALESCE(counterparty_name, ''),
    COALESCE(contract_name, ''),
    COALESCE(debit_account, ''),
    COALESCE(document, ''),
    COALESCE(analytics_dt, ''),
    COALESCE(analytics_kt, '')
  );
