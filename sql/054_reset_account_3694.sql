--
-- Контекст: при сверке с карточкой сч.51 (полный период 01.01.2022 — 22.04.2026)
-- баланс в портале расходится с эталоном на 4 598 041,90 ₽.
-- Локализация показала:
--   • 25 «осиротевших» приходов от СЗ ГАЛС-ФРИДРИХА ЭНГЕЛЬСА с NULL bank_account_id
--     (парсер не привязал к р/с — частичный/повторный импорт);
--   • потеря 5 Кт-строк за июнь 2025 на 1 770 941,90 ₽;
--   • потеря 11 Кт-строк за март 2026 на 2 838 000,00 ₽;
--   • расхождение по 2 строкам в окт/нояб 2025 на 10 900,00 ₽
--     (амплитуда отличия от эталона).
-- После 052 (опечатка) и 053 (opening_balance) сами правила парсинга исправлены,
-- поэтому достаточно перезагрузить только эти строки.
--
-- Цель миграции — подготовить БД к чистому переимпорту:
--   1) сохранить уже накопленные ручные/автоматические маршруты в etl_1c_contract_map,
--      чтобы при переимпорте автороут восстановил «контрагент+договор → проект»;
--   2) удалить все etl_1c_entries с этим bank_account_id;
--   3) удалить «осиротевшие» NULL-строки этого счёта.
--
-- ⚠️ После миграции — перезалить файл `Карточка счета 51 3694.xlsx`
-- через ETL-шлюз 1С в портале. Эталонный остаток после переимпорта =
-- 2 847 304,19 ₽ на 22.04.2026.

BEGIN;

-- 1) Сохранить связки «контрагент + договор → проект» из уже разнесённых записей,
--    чтобы переимпорт автоматически восстановил привязку к проектам в БДДС.
INSERT INTO etl_1c_contract_map (counterparty_name, contract_name, project_id)
SELECT DISTINCT counterparty_name, contract_name, routed_project_id
  FROM etl_1c_entries
 WHERE bank_account_id    = (SELECT id FROM bank_accounts WHERE account_number = '40702810225644213694')
   AND routed_project_id IS NOT NULL
   AND counterparty_name  IS NOT NULL
   AND contract_name      IS NOT NULL
ON CONFLICT (counterparty_name, contract_name) DO NOTHING;

-- 2) Удалить «осиротевшие» NULL-строки от СЗ ГАЛС, в аналитике которых наш счёт
DELETE FROM etl_1c_entries
 WHERE bank_account_id IS NULL
   AND analytics_dt LIKE '40702810225644213694,%'
   AND doc_type = 'receipt';

-- 3) Удалить все ETL-записи по нашему р/с
DELETE FROM etl_1c_entries
 WHERE bank_account_id = (SELECT id FROM bank_accounts WHERE account_number = '40702810225644213694');

-- 4) (опционально) Сбросить дедуп-индекс, чтобы переимпорт не упёрся в старые
--    хеши (если 045_dedup_index_add_document.sql ввёл уникальный индекс).
--    Безопасно: индекс пересоздастся на новых данных при следующем импорте.
